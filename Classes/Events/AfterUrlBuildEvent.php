<?php

declare(strict_types=1);

namespace In2code\Fetchurl\Events;

final class AfterUrlBuildEvent
{
    private string $url;

    public function __construct(string $url)
    {
        $this->url = $url;
    }

    public function getUrl(): string
    {
        return $this->url;
    }

    public function setUrl(string $url): void
    {
        $this->url = $url;
    }
}